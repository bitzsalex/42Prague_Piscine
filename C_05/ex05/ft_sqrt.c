int	ft_sqrt(int nb);

int	ft_sqrt(int nb)
{
	int	itr;

	if (nb > 0)
	{
		itr = 1;
		while (itr <= (nb / 2) + 1)
		{
			if (itr * itr == nb)
				return (itr);
			itr++;
		}
	}
	return (0);
}
